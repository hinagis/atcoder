fn main() {
    proconio::input! {
        n: u64,
    }

    if n <= 999 {
        println!("0");
    } else if n <= 999_999 {
        println!("{}", n - 999);
    } else if n <= 999_999_999 {
        println!("{}", (n - 999_999) * 2 + 999_000);
    } else if n <= 999_999_999_999 {
        println!("{}", (n - 999_999_999) * 3 + 999_000_000 * 2 + 999_000);
    } else if n <= 999_999_999_999_999 {
        println!("{}", (n - 999_999_999_999) * 4 + 999_000_000_000 * 3 + 999_000_000 * 2 + 999_000);
    } else {
        println!("{}", 5u64 + 999_000_000_000_000 * 4 + 999_000_000_000 * 3 + 999_000_000 * 2 + 999_000);
    }
}
